import { F } from "./14";
test("should return correct results", () => {
  expect(F(5, [8, 10, 1, 9, 6])).toEqual([10, 9, 8, 1, 6]);
  expect(F(9, [5.16, 0.3, 2.57, 4.2, 1.45, 4.65, 5.91, 2.73, 3.89])).toEqual([
    0.3,
    4.2,
    4.65,
    2.73,
    5.16,
    2.57,
    1.45,
    5.91,
    3.89
  ]);
  expect(F(6, [8.89, 2.15, 2.63, 5.47, 9.53, 0.7])).toEqual([
    2.15,
    5.47,
    0.7,
    8.89,
    2.63,
    9.53
  ]);
});
