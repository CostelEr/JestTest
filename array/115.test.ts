import { F } from "./115";
test("should return correct results", () => {
  expect(
    F(10, [-6.23, 7.54, 7.76, 8.68, -6.49, 9.9, -5.53, -0.66, 6.13, -2.24])
  ).toEqual([5, 1, 7, 10, 8, 9, 2, 3, 4, 6]);
  expect(
    F(9, [2.66, 7.31, -1.18, -6.34, 1.12, 3.39, -4.16, -8.19, 6.03])
  ).toEqual([8, 4, 7, 3, 5, 1, 6, 9, 2]);
  expect(F(9, [2.86, -4.45, 4.08, -9, 4.98, 8, -6.91, -3.08, -3.87])).toEqual([
    4,
    7,
    2,
    9,
    8,
    1,
    3,
    5,
    6
  ]);
});
