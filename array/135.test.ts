import { F } from "./135";
test("should return correct results", () => {
  expect(
    F(
      6,
      [
        { x: 5.36, y: 3.82 },
        { x: 7.18, y: -4.62 },
        { x: -3.09, y: -8.34 },
        { x: 6.75, y: 3.35 },
        { x: 7.12, y: -6.88 },
        { x: -0.58, y: 1.66 }
      ],
      8,
      [
        { x: -5.07, y: -0.08 },
        { x: 5.3, y: -6.05 },
        { x: 8.41, y: -2.69 },
        { x: 8.52, y: 5.33 },
        { x: -8.49, y: -9.72 },
        { x: -2.91, y: 5.99 },
        { x: -0.08, y: -2.02 },
        { x: 3.62, y: -1.87 }
      ]
    )
  ).toEqual([2.0, { x: 7.12, y: -6.88 }, { x: 5.3, y: -6.05 }]);

  expect(
    F(
      6,
      [
        { x: -4.39, y: -8.84 },
        { x: -7.48, y: -5.39 },
        { x: -8.48, y: 4.77 },
        { x: 1.72, y: -0.5 },
        { x: -2.65, y: -3.67 },
        { x: 8.42, y: 3.93 }
      ],
      4,
      [
        { x: 5.41, y: 1.69 },
        { x: -0.6, y: -6.46 },
        { x: -2.81, y: -9.27 },
        { x: -9.3, y: -2.54 }
      ]
    )
  ).toEqual([1.64, { x: -4.39, y: -8.84 }, { x: -2.81, y: -9.27 }]);

  expect(
    F(
      8,
      [
        { x: 2.97, y: 4.36 },
        { x: 2.17, y: -9.7 },
        { x: -5.6, y: -1.56 },
        { x: -9.74, y: 3.51 },
        { x: 6.34, y: -3.85 },
        { x: -6.94, y: -9.09 },
        { x: 0.98, y: -7.94 },
        { x: -2.8, y: 9.66 }
      ],
      4,
      [
        { x: -0.01, y: 7.49 },
        { x: 8.9, y: 3.57 },
        { x: 0.36, y: -6.83 },
        { x: 5.64, y: -5.13 }
      ]
    )
  ).toEqual([1.27, { x: 0.98, y: -7.94 }, { x: 0.36, y: -6.83 }]);
});
