import { F } from "./30";
test("should return correct results", () => {
  expect(F(7, [-8.56, 6.7, -2.02, 8.07, 9.42, 5.41, -8.62])).toEqual([
    2,
    5,
    6,
    3
  ]);
  expect(
    F(10, [-9.8, -7.81, -6.79, -2.58, -2.39, -1.89, 1.36, 3.69, 4.88, 5.28])
  ).toEqual([0]);
  expect(F(6, [-5.59, 7.53, 8.04, -2.05, -4.02, -7.58])).toEqual([3, 4, 5, 3]);
});
