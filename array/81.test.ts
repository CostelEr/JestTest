import { F } from "./81";
test("should return correct results", () => {
  expect(F(7, [-2.96, 0.21, 0.97, 6.27, -9.79, -8.13, 9.97], 4)).toEqual([
    0,
    0,
    0,
    0,
    -2.96,
    0.21,
    0.97
  ]);
  expect(F(8, [-6.62, -4.19, -5.32, 9.41, 3.98, 0.59, 5.33, -3.52], 5)).toEqual(
    [0, 0, 0, 0, 0, -6.62, -4.19, -5.32]
  );
  expect(
    F(9, [2.92, -0.34, 8.39, 5.4, 1.09, -1.96, 7.56, 5.24, -2.14], 2)
  ).toEqual([0, 0, 2.92, -0.34, 8.39, 5.4, 1.09, -1.96, 7.56]);
});
