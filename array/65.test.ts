import { F } from "./65";
test("should return correct results", () => {
  expect(
    F(10, [6.86, 5.14, 8.17, 3.25, 7.73, 6.84, 8.34, 4.3, 1.2, 3.17], 4)
  ).toEqual([10.11, 8.39, 11.42, 6.5, 10.98, 10.09, 11.59, 7.55, 4.45, 6.42]);
  expect(
    F(10, [3.63, 8.66, 8.6, 5.9, 9.73, 6.64, 1.74, 9.83, 8.75, 2.47], 8)
  ).toEqual([
    13.46,
    18.49,
    18.43,
    15.73,
    19.56,
    16.47,
    11.57,
    19.66,
    18.58,
    12.3
  ]);
  expect(F(5, [4.69, 6.64, 8.17, 6.81, 8.94], 1)).toEqual([
    9.38,
    11.33,
    12.86,
    11.5,
    13.63
  ]);
});
