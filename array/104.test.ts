import { F } from "./104";
test("should return correct results", () => {
  expect(F(6, [-2.43, -7.31, -2.72, -8.25, -0.93, 3.08], 3, 3)).toEqual([
    -2.43,
    -7.31,
    0,
    0,
    0,
    -2.72,
    -8.25,
    -0.93,
    3.08
  ]);
  expect(F(7, [-0.54, -1.94, -6.32, -8.78, -8.16, 4.13, -1.3], 3, 2)).toEqual([
    -0.54,
    -1.94,
    0,
    0,
    -6.32,
    -8.78,
    -8.16,
    4.13,
    -1.3
  ]);
  expect(
    F(8, [0.73, -1.44, 1.63, -7.92, -8.21, -7.45, -2.98, -5.07], 7, 2)
  ).toEqual([0.73, -1.44, 1.63, -7.92, -8.21, -7.45, 0, 0, -2.98, -5.07]);
  expect(
    F(8, [0.73, -1.44, 1.63, -7.92, -8.21, -7.45, -2.98, -5.07], 1, 5)
  ).toEqual([
    0,
    0,
    0,
    0,
    0,
    0.73,
    -1.44,
    1.63,
    -7.92,
    -8.21,
    -7.45,
    -2.98,
    -5.07
  ]);
});
