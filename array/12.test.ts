import { F } from "./12";
test("should return correct results", () => {
  expect(F(4, [8, 10, 1, 9])).toEqual([10, 9]);
  expect(F(6, [4.61, 6.15, 2.25, 3.47, 9.06, 6.47])).toEqual([
    6.15,
    3.47,
    6.47
  ]);
  expect(F(8, [1.51, 4.15, 2.72, 7.13, 3.47, 1.0, 3.87, 6.76])).toEqual([
    4.15,
    7.13,
    1.0,
    6.76
  ]);
  expect(F(4, [9.71, 2.09, 1.38, 1.33])).toEqual([2.09, 1.33]);
  expect(F(2, [9.74, 6.93])).toEqual([6.93]);
  expect(
    F(10, [2.02, 0.29, 3.21, 0.88, 1.68, 8.0, 2.89, 3.78, 5.4, 4.69])
  ).toEqual([0.29, 0.88, 8.0, 3.78, 4.69]);
});
