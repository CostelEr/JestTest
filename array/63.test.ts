import { F } from "./63";
test("should return correct results", () => {
  expect(
    F([-9.84, -0.29, 0.58, 1.27, 6.97], [-9.54, -5.27, 0.79, 4.64, 6.58])
  ).toEqual([-9.84, -9.54, -5.27, -0.29, 0.58, 0.79, 1.27, 4.64, 6.58, 6.97]);
  expect(
    F([-8.28, -7.03, 2.62, 2.63, 9.41], [-1.98, -0.22, 1.71, 3.45, 5.96])
  ).toEqual([-8.28, -7.03, -1.98, -0.22, 1.71, 2.62, 2.63, 3.45, 5.96, 9.41]);
  expect(
    F([-9.7, -8.08, -4.37, -2.92, 4.92], [-9.96, -8.78, -4.8, -3.18, 7.63])
  ).toEqual([-9.96, -9.7, -8.78, -8.08, -4.8, -4.37, -3.18, -2.92, 4.92, 7.63]);
});
