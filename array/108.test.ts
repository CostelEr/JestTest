import { F } from "./108";
test("should return correct results", () => {
  expect(F(3, [6.8, 8.04, -1.85])).toEqual([0, 6.8, 0, 8.04, -1.85]);
  expect(F(5, [5.66, 5.86, -4.52, 3.76, -3.7])).toEqual([
    0,
    5.66,
    0,
    5.86,
    -4.52,
    0,
    3.76,
    -3.7
  ]);
  expect(F(5, [-4.92, 7.86, -8.87, -0.48, 4.58])).toEqual([
    -4.92,
    0,
    7.86,
    -8.87,
    -0.48,
    0,
    4.58
  ]);
});
