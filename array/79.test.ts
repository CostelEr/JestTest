import { F } from "./79";
test("should return correct results", () => {
  expect(
    F(9, [-8.82, -6.98, 2.55, -9.02, -2.49, 1.39, 7.68, -8.45, 8.44])
  ).toEqual([0, -8.82, -6.98, 2.55, -9.02, -2.49, 1.39, 7.68, -8.45]);
  expect(F(9, [3.25, -8.22, 2.5, 4.67, 1.65, 0.17, 1.05, -8.49, 3.15])).toEqual(
    [0, 3.25, -8.22, 2.5, 4.67, 1.65, 0.17, 1.05, -8.49]
  );
  expect(F(8, [7.78, 9.2, -5.97, 6.55, -4.53, -4.07, 6.61, -9.12])).toEqual([
    0,
    7.78,
    9.2,
    -5.97,
    6.55,
    -4.53,
    -4.07,
    6.61
  ]);
});
