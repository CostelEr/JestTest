import { F } from "./101";
test("should return correct results", () => {
  expect(F(6, [1.09, -3.99, 8.63, 3.58, 9.37, 0.15], 4)).toEqual([
    1.09,
    -3.99,
    8.63,
    0,
    3.58,
    9.37,
    0.15
  ]);
  expect(
    F(9, [4.19, 9.85, 9.57, 6.39, -6.85, 2.56, 4.81, 0.43, 8.86], 5)
  ).toEqual([4.19, 9.85, 9.57, 6.39, 0, -6.85, 2.56, 4.81, 0.43, 8.86]);
  expect(F(5, [-2.82, -1.71, -3.08, -2.6, -9.16], 5)).toEqual([
    -2.82,
    -1.71,
    -3.08,
    -2.6,
    0,
    -9.16
  ]);
  expect(F(5, [-2.82, -1.71, -3.08, -2.6, -9.16], 1)).toEqual([
    0,
    -2.82,
    -1.71,
    -3.08,
    -2.6,
    -9.16
  ]);
});
