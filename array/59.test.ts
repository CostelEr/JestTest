import { F } from "./59";
test("should return correct results", () => {
  expect(F(4, [-9.76, -3.36, -0.98, -9.48])).toEqual([
    -9.76,
    -6.56,
    -4.7,
    -5.89
  ]);
  expect(F(5, [2.41, -6.61, 0.31, 4.26, 9.38])).toEqual([
    2.41,
    -2.1,
    -1.3,
    0.09,
    1.95
  ]);
  expect(
    F(10, [6.18, -9.3, -0.77, -3.58, 6.48, -3.21, -4.58, 0.69, -7.9, -5.46])
  ).toEqual([6.18, -1.56, -1.3, -1.87, -0.2, -0.7, -1.25, -1.01, -1.78, -2.15]);
});
