import { F } from "./86";
test("should return correct results", () => {
  expect(F(5, [0.51, 3.53, -0.4, 0.2, 7.14], 3)).toEqual([
    0.2,
    7.14,
    0.51,
    3.53,
    -0.4
  ]);
  expect(
    F(10, [0.68, 2.71, 7.17, -3.13, 5.92, -1.11, -9.65, 7.06, 4.67, -8.92], 4)
  ).toEqual([5.92, -1.11, -9.65, 7.06, 4.67, -8.92, 0.68, 2.71, 7.17, -3.13]);
  expect(F(7, [-8.49, 4.57, -1.02, 7.79, -2.25, -7.75, 9.67], 1)).toEqual([
    4.57,
    -1.02,
    7.79,
    -2.25,
    -7.75,
    9.67,
    -8.49
  ]);
});
