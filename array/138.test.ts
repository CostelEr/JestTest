import { F } from "./138";
test("should return correct results", () => {
  expect(
    F(8, [
      { x: 5.88, y: 1.62 },
      { x: -8.19, y: 8.36 },
      { x: 6.18, y: 8.27 },
      { x: -0.41, y: 3.51 },
      { x: 1.16, y: -9.34 },
      { x: 5.46, y: 5.73 },
      { x: -9.84, y: -7.13 },
      { x: 2.17, y: -5.76 }
    ])
  ).toEqual([
    13.43,
    { x: 5.88, y: 1.62 },
    { x: 6.18, y: 8.27 },
    { x: 5.46, y: 5.73 }
  ]);

  expect(
    F(7, [
      { x: 1.5, y: -9.77 },
      { x: 0.27, y: 3.65 },
      { x: -5.63, y: 3.88 },
      { x: 9.75, y: 8.64 },
      { x: 6.07, y: -9.63 },
      { x: -5.97, y: -9.82 },
      { x: -0.94, y: 3.16 }
    ])
  ).toEqual([
    11.95,
    { x: 0.27, y: 3.65 },
    { x: -5.63, y: 3.88 },
    { x: -0.94, y: 3.16 }
  ]);

  expect(
    F(8, [
      { x: -6.9, y: -6.67 },
      { x: -7.75, y: -1.1 },
      { x: -6.65, y: 0.4 },
      { x: 1.15, y: -7.12 },
      { x: -0.77, y: -5.6 },
      { x: -3.8, y: 4.1 },
      { x: 9.6, y: 8.45 },
      { x: -7.85, y: 7 }
    ])
  ).toEqual([
    13.06,
    { x: -7.75, y: -1.1 },
    { x: -6.65, y: 0.4 },
    { x: -3.8, y: 4.1 }
  ]);
});
