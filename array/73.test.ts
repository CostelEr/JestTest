import { F } from "./73";
test("should return correct results", () => {
  expect(F(5, [4.2, 1.02, 7.87, 9.72, -8.87], 1, 5)).toEqual([
    4.2,
    9.72,
    7.87,
    1.02,
    -8.87
  ]);
  expect(F(5, [4.2, 1.02, 7.87, 9.72, -8.87], 1, 4)).toEqual([
    4.2,
    7.87,
    1.02,
    9.72,
    -8.87
  ]);
  expect(F(7, [-3.56, -4.43, -5.46, -1.6, 8.66, -9.91, 9], 2, 3)).toEqual([
    -3.56,
    -4.43,
    -5.46,
    -1.6,
    8.66,
    -9.91,
    9
  ]);
  expect(
    F(
      10,
      [-2.23, 9.23, 3.53, -1.52, 9.49, 5.82, -2.15, -3.84, -5.1, 9.76],
      5,
      8
    )
  ).toEqual([-2.23, 9.23, 3.53, -1.52, 9.49, -2.15, 5.82, -3.84, -5.1, 9.76]);
  expect(F(8, [6.8, 7.94, 7.4, -5.9, 1.69, -7.65, 3.15, 0.9], 2, 7)).toEqual([
    6.8,
    7.94,
    -7.65,
    1.69,
    -5.9,
    7.4,
    3.15,
    0.9
  ]);
});
