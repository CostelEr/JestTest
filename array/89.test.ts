import { F } from "./89";
test("should return correct results", () => {
  expect(
    F(10, [9.05, -4.2, 8.22, 3.5, -1.85, -2.54, -3.29, -3.91, -4.21, -4.3])
  ).toEqual([9.05, 8.22, 3.5, -1.85, -2.54, -3.29, -3.91, -4.2, -4.21, -4.3]);
  expect(F(7, [-3.08, -3.85, -5.37, -6.19, 0.29, -9.22, -9.67])).toEqual([
    0.29,
    -3.08,
    -3.85,
    -5.37,
    -6.19,
    -9.22,
    -9.67
  ]);
  expect(F(8, [6.82, 6.59, 1.34, 0.23, -1.8, 6.18, -2.3, -6.29])).toEqual([
    6.82,
    6.59,
    6.18,
    1.34,
    0.23,
    -1.8,
    -2.3,
    -6.29
  ]);
});
