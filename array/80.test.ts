import { F } from "./80";
test("should return correct results", () => {
  expect(F(3, [2.06, 0.9, 8.6])).toEqual([0.9, 8.6, 0]);
  expect(F(6, [-4.07, 7.64, 1.99, -9.91, 5.65, -9.89])).toEqual([
    7.64,
    1.99,
    -9.91,
    5.65,
    -9.89,
    0
  ]);
  expect(F(5, [4.86, 4.86, -1.9, -1.69, -7.28])).toEqual([
    4.86,
    -1.9,
    -1.69,
    -7.28,
    0
  ]);
});
