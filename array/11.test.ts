import { F } from "./11";
test("should return correct results", () => {
  expect(F(4, [8, 10, 1, 9], 2)).toEqual([10, 9]);
  expect(F(6, [4.61, 6.15, 2.25, 3.47, 9.06, 6.47], 2)).toEqual([
    6.15,
    3.47,
    6.47
  ]);
  expect(F(8, [1.51, 4.15, 2.72, 7.13, 3.47, 1.0, 3.87, 6.76], 3)).toEqual([
    2.72,
    1.0
  ]);
  expect(F(7, [6.01, 3.08, 8.31, 5.63, 6.97, 6.27, 1.95], 3)).toEqual([
    8.31,
    6.27
  ]);
});
