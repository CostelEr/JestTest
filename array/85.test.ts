import { F } from "./85";
test("should return correct results", () => {
  expect(F(5, [-5.68, 0, 1.16, -5.11, 8.6], 2)).toEqual([
    -5.11,
    8.6,
    -5.68,
    0,
    1.16
  ]);
  expect(F(7, [4.45, 1.89, 6.36, 8.64, -8.83, 2.33, -8.03], 3)).toEqual([
    -8.83,
    2.33,
    -8.03,
    4.45,
    1.89,
    6.36,
    8.64
  ]);
  expect(F(6, [5.18, 8.3, 0.81, -5.37, 0.17, -4.7], 3)).toEqual([
    -5.37,
    0.17,
    -4.7,
    5.18,
    8.3,
    0.81
  ]);
});
