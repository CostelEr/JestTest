import { F } from "./13";
test("should return correct results", () => {
  expect(F(5, [8, 10, 1, 9, 6])).toEqual([6, 1, 8]);
  expect(F(9, [3.33, 1.96, 8.12, 8.45, 1.32, 3.5, 4.95, 3.87, 2.83])).toEqual([
    2.83,
    4.95,
    1.32,
    8.12,
    3.33
  ]);
  expect(F(3, [0.31, 8.19, 5.22])).toEqual([5.22, 0.31]);
  expect(F(7, [3.22, 1.04, 7.25, 7.33, 2.6, 5.17, 1.92])).toEqual([
    1.92,
    2.6,
    7.25,
    3.22
  ]);
});
