import { F } from "./46";
test("should return correct results", () => {
  expect(F(6, 7, [2, 3, 3, 5, 6, 7, 8])).toEqual([3, 3]);
  expect(
    F(-6.95, 9, [-3.07, -3.39, 8.91, 2.21, 3.28, 5.32, 3.24, -2.98, 1.09])
  ).toEqual([-3.07, -3.39]);
  expect(
    F(-1.86, 10, [
      5.36,
      4.64,
      -3.56,
      -3.12,
      -5.74,
      0.4,
      9.49,
      1.85,
      -1.69,
      1.11
    ])
  ).toEqual([-3.12, 1.11]);
  expect(F(8.76, 6, [-8.26, -8.62, 3.33, 7, 3.68, -0.56])).toEqual([3.33, 7]);
});
