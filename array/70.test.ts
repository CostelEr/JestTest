import { F } from "./70";
test("should return correct results", () => {
  expect(
    F(10, [-8.33, -4.96, -8.84, -9.59, -3.27, 8.07, -5.49, -4.98, 5.04, 5.08])
  ).toEqual([
    8.07,
    -5.49,
    -4.98,
    5.04,
    5.08,
    -8.33,
    -4.96,
    -8.84,
    -9.59,
    -3.27
  ]);
  expect(F(8, [0.03, 7.2, 4.37, 6.61, -5.45, -3.02, 3.76, -0.46])).toEqual([
    -5.45,
    -3.02,
    3.76,
    -0.46,
    0.03,
    7.2,
    4.37,
    6.61
  ]);
  expect(F(6, [9.84, -2.35, 9.49, -2.05, -4.78, 8.18])).toEqual([
    -2.05,
    -4.78,
    8.18,
    9.84,
    -2.35,
    9.49
  ]);
});
