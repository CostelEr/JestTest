import { F } from "./33";
test("should return correct results", () => {
  expect(
    F(10, [6.57, 6.21, 5.91, 5.54, 5.15, 4.9, 4.63, 4.26, 4.08, 3.7])
  ).toEqual(1);
  expect(
    F(10, [1.17, 1.44, 0.49, 6.72, 8.64, 3.2, 9.18, 0.97, 3.97, 4.66])
  ).toEqual(10);
  expect(F(5, [1.74, 2.16, 3.25, 2.95, 2.66])).toEqual(3);
  expect(F(9, [8.53, 7.29, 3.48, 6.79, 0.33, 9.47, 2.88, 3.03, 2.75])).toEqual(
    8
  );
});
