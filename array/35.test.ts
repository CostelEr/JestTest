import { F } from "./35";
test("should return correct results", () => {
  expect(
    F(10, [6.97, -2.15, 6.17, -5.83, -6.59, 8.28, -7.7, 5.95, -3.54, -5.4])
  ).toEqual(5.95);
  expect(
    F(10, [4.73, -0.44, 9.8, 9.69, -2.52, 8.18, -2.47, -9.05, -3.7, 2.96])
  ).toEqual(2.96);
  expect(F(6, [-2.19, -0.79, 7.12, 4.74, -3.74, 1.31])).toEqual(1.31);
});
