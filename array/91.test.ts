import { F } from "./91";
test("should return correct results", () => {
  expect(
    F(10, [3.62, 0.87, 8.81, -1.9, 1.11, 9.98, 1.51, -9.4, -3.01, -2.09], 2, 10)
  ).toEqual([1, [3.62]]);
  expect(
    F(8, [-0.77, -2.26, 7.05, 2.17, 8.39, -2.49, 2.41, -4.62], 1, 5)
  ).toEqual([3, [-2.49, 2.41, -4.62]]);
  expect(F(6, [6.87, -5.74, 1.61, 7.08, 5.1, 8.99], 2, 3)).toEqual([
    4,
    [6.87, 7.08, 5.1, 8.99]
  ]);
});
