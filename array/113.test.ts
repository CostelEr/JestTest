import { F } from "./113";
test("should return correct results", () => {
  expect(F(3, [5.54, -2.64, 6.68])).toEqual([
    [5.54, -2.64, 6.68],
    [-2.64, 5.54, 6.68]
  ]);
  expect(F(5, [-6.56, -8.36, 1.62, 4.09, -7.71])).toEqual([
    [-6.56, -8.36, 1.62, -7.71, 4.09],
    [-6.56, -8.36, -7.71, 1.62, 4.09],
    [-7.71, -8.36, -6.56, 1.62, 4.09],
    [-8.36, -7.71, -6.56, 1.62, 4.09]
  ]);
  expect(F(6, [3.47, 1.84, 1.63, 8.03, 1.46, 1.16])).toEqual([
    [3.47, 1.84, 1.63, 1.16, 1.46, 8.03],
    [1.46, 1.84, 1.63, 1.16, 3.47, 8.03],
    [1.46, 1.16, 1.63, 1.84, 3.47, 8.03],
    [1.46, 1.16, 1.63, 1.84, 3.47, 8.03],
    [1.16, 1.46, 1.63, 1.84, 3.47, 8.03]
  ]);
});
