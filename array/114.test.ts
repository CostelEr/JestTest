import { F } from "./114";
test("should return correct results", () => {
  expect(F(5, [6.75, -3.04, -9.24, 2.01, -3.89])).toEqual([
    [-3.04, 6.75, -9.24, 2.01, -3.89],
    [-9.24, -3.04, 6.75, 2.01, -3.89],
    [-9.24, -3.04, 2.01, 6.75, -3.89],
    [-9.24, -3.89, -3.04, 2.01, 6.75]
  ]);
  expect(F(3, [-0.09, -6.76, 1.02])).toEqual([
    [-6.76, -0.09, 1.02],
    [-6.76, -0.09, 1.02]
  ]);
  expect(F(6, [-1.78, -4.49, 5.47, -5.77, 4.62, -6.02])).toEqual([
    [-4.49, -1.78, 5.47, -5.77, 4.62, -6.02],
    [-4.49, -1.78, 5.47, -5.77, 4.62, -6.02],
    [-5.77, -4.49, -1.78, 5.47, 4.62, -6.02],
    [-5.77, -4.49, -1.78, 4.62, 5.47, -6.02],
    [-6.02, -5.77, -4.49, -1.78, 4.62, 5.47]
  ]);
});
