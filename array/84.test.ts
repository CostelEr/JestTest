import { F } from "./84";
test("should return correct results", () => {
  expect(
    F(9, [-6.89, -6.13, 4.67, 0.84, -7.64, 9.79, -6.59, 2.99, 2.74])
  ).toEqual([-6.13, 4.67, 0.84, -7.64, 9.79, -6.59, 2.99, 2.74, -6.89]);
  expect(F(8, [3.6, 5.83, 2.28, -0.36, -8.97, 0.25, -0.21, 8.55])).toEqual([
    5.83,
    2.28,
    -0.36,
    -8.97,
    0.25,
    -0.21,
    8.55,
    3.6
  ]);
  expect(F(7, [8.49, -8.04, 4.76, -4.05, -1.73, 1.72, 1.11])).toEqual([
    -8.04,
    4.76,
    -4.05,
    -1.73,
    1.72,
    1.11,
    8.49
  ]);
});
