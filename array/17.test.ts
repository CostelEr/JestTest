import { F } from "./17";
test("should return correct results", () => {
  /* expect(F(5, [8, 10, 7, 1, 9])).toEqual([8, 9, 10, 1, 7]);*/
  expect(
    F(10, [5.34, 3.93, 3.74, 5.46, 2.42, 6.21, 8.95, 6.84, 6.98, 3.34])
  ).toEqual([5.34, 3.93, 3.34, 6.98, 3.74, 5.46, 6.84, 8.95, 2.42, 6.21]);
  expect(F(5, [7.6, 3.67, 7.32, 0.42, 1.64])).toEqual([
    7.6,
    3.67,
    1.64,
    0.42,
    7.32
  ]);
  expect(F(7, [7.44, 6.31, 6.69, 2.52, 1.68, 2.74, 1.31])).toEqual([
    7.44,
    6.31,
    1.31,
    2.74,
    6.69,
    2.52,
    1.68
  ]);
});
