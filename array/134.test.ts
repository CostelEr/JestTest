import { F } from "./134";
test("should return correct results", () => {
  expect(
    F(7, [
      { x: 0.05, y: 8.27 },
      { x: -1.28, y: 8.36 },
      { x: 4.81, y: -3.13 },
      { x: -4.06, y: -1.54 },
      { x: -1.46, y: -2.31 },
      { x: 0.64, y: 3.16 },
      { x: -2.49, y: -1.46 }
    ])
  ).toEqual([{ x: -1.28, y: 8.36 }, { x: 4.81, y: -3.13 }, 13.0]);

  expect(
    F(4, [
      { x: -1.41, y: 9.52 },
      { x: -2.14, y: 4.23 },
      { x: 9, y: 7.45 },
      { x: 7.69, y: 9.86 }
    ])
  ).toEqual([{ x: -2.14, y: 4.23 }, { x: 9, y: 7.45 }, 11.6]);

  expect(
    F(5, [
      { x: -8.46, y: -3.56 },
      { x: -7.96, y: 7.54 },
      { x: 8.16, y: 8.38 },
      { x: 9.57, y: -7.1 },
      { x: 6.29, y: -7.68 }
    ])
  ).toEqual([{ x: -7.96, y: 7.54 }, { x: 9.57, y: -7.1 }, 22.84]);
});
