import { F } from "./132";
test("should return correct results", () => {
  expect(
    F(4, [
      { x: -6.82, y: 3.86 },
      { x: -5.76, y: -6.63 },
      { x: 3.53, y: 3.06 },
      { x: 9.26, y: -5.89 }
    ])
  ).toEqual({ x: -6.82, y: 3.86 });

  expect(
    F(8, [
      { x: 2.16, y: -9.14 },
      { x: 1.74, y: 5.04 },
      { x: 9.38, y: -7.14 },
      { x: -3.22, y: 4.6 },
      { x: 5.03, y: 0.26 },
      { x: -0.3, y: 8.01 },
      { x: 3.84, y: -9.26 },
      { x: -6.86, y: -4.43 }
    ])
  ).toEqual({ x: -0.3, y: 8.01 });

  expect(
    F(8, [
      { x: 2.16, y: -9.14 },
      { x: 1.74, y: 5.04 },
      { x: 9.38, y: -7.14 },
      { x: 3.22, y: 4.6 },
      { x: 5.03, y: 0.26 },
      { x: 0.3, y: 8.01 },
      { x: 3.84, y: -9.26 },
      { x: -6.86, y: -4.43 }
    ])
  ).toEqual({ x: 0, y: 0 });
});
