import { F } from "./87";
test("should return correct results", () => {
  expect(F(5, [-2.71, -9.1, -1.29, 2.68, 9.33])).toEqual([
    -9.1,
    -2.71,
    -1.29,
    2.68,
    9.33
  ]);
  expect(F(6, [-6.1, -8.27, -7.7, -5.63, -3.31, 2.84])).toEqual([
    -8.27,
    -7.7,
    -6.1,
    -5.63,
    -3.31,
    2.84
  ]);
  expect(F(5, [9.01, -7.81, -6.87, 3.01, 3.05])).toEqual([
    -7.81,
    -6.87,
    3.01,
    3.05,
    9.01
  ]);
});
