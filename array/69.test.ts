import { F } from "./69";
test("should return correct results", () => {
  expect(F(4, [-0.33, 4.13, 6.27, 0.15])).toEqual([4.13, -0.33, 0.15, 6.27]);
  expect(
    F(10, [-1.65, -5.15, -1.66, 8.04, -1.16, -5.72, -4.56, 8.49, -7.57, 1.34])
  ).toEqual([
    -5.15,
    -1.65,
    8.04,
    -1.66,
    -5.72,
    -1.16,
    8.49,
    -4.56,
    1.34,
    -7.57
  ]);
  expect(F(8, [-7.22, 8.52, 4.32, -0.56, -4.14, 8.86, 5.83, 2.39])).toEqual([
    8.52,
    -7.22,
    -0.56,
    4.32,
    8.86,
    -4.14,
    2.39,
    5.83
  ]);
});
