import { F } from "./136";
test("should return correct results", () => {
  expect(
    F(7, [
      { x: -2.09, y: -6.72 },
      { x: -3.13, y: 3.66 },
      { x: 2.3, y: 5.82 },
      { x: -4.5, y: -9.09 },
      { x: 2.72, y: -2.1 },
      { x: 8.83, y: -1 },
      { x: -8.38, y: -8.03 }
    ])
  ).toEqual([{ x: 2.72, y: -2.1 }, 51.65]);

  expect(
    F(4, [
      { x: 8.61, y: 4.83 },
      { x: 8.35, y: -2.31 },
      { x: 5.27, y: 5.43 },
      { x: 9.48, y: -8.29 }
    ])
  ).toEqual([{ x: 8.35, y: -2.31 }, 21.56]);

  expect(
    F(4, [
      { x: 8.61, y: 4.83 },
      { x: 8.35, y: -2.31 },
      { x: 5.27, y: 5.43 },
      { x: 9.48, y: -8.29 }
    ])
  ).toEqual([{ x: 8.35, y: -2.31 }, 21.56]);

  expect(
    F(4, [
      { x: -0.11, y: -4.46 },
      { x: -9.61, y: -0.73 },
      { x: 0.5, y: -6.57 },
      { x: 9.77, y: 5.19 }
    ])
  ).toEqual([{ x: -0.11, y: -4.46 }, 26.21]);
});
