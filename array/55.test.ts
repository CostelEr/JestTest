import { F } from "./55";
test("should return correct results", () => {
  expect(F(3, [29, 82, 5])).toEqual([2, [29, 5]]);
  expect(F(2, [29, 82])).toEqual([1, [29]]);
  expect(F(12, [28, 92, -89, 83, 84, -43, 25, 47, 7, 78, 51, 61])).toEqual([
    6,
    [28, -89, 84, 25, 7, 51]
  ]);
  expect(
    F(14, [-23, 50, -73, -56, 76, -75, 0, 87, -24, 35, 85, 50, 62, -38])
  ).toEqual([7, [-23, -73, 76, 0, -24, 85, 62]]);
});
