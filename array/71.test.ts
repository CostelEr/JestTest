import { F } from "./71";
test("should return correct results", () => {
  expect(F(9, [7.54, -7.86, 3.38, 6.41, 7.5, 7.04, 2.06, 3.8, -9.16])).toEqual([
    -9.16,
    3.8,
    2.06,
    7.04,
    7.5,
    6.41,
    3.38,
    -7.86,
    7.54
  ]);
  expect(F(4, [-8.76, 0.95, -1.04, -5.41])).toEqual([
    -5.41,
    -1.04,
    0.95,
    -8.76
  ]);
  expect(F(7, [-3.56, 8.68, 1.84, -6.88, -7.01, -6.28, 8.84])).toEqual([
    8.84,
    -6.28,
    -7.01,
    -6.88,
    1.84,
    8.68,
    -3.56
  ]);
  expect(F(2, [1.52, -8.39])).toEqual([-8.39, 1.52]);
});
