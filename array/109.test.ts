import { F } from "./109";
test("should return correct results", () => {
  expect(F(5, [7.78, 0.4, -8.51, -6.63, -7.43])).toEqual([
    7.78,
    0.4,
    -8.51,
    0,
    -6.63,
    0,
    -7.43,
    0
  ]);
  expect(F(4, [1.76, -9.58, 5.28, 1.25])).toEqual([1.76, -9.58, 0, 5.28, 1.25]);
  expect(F(5, [-7.1, -2.71, 6.14, -0.14, -4.25])).toEqual([
    -7.1,
    0,
    -2.71,
    0,
    6.14,
    -0.14,
    0,
    -4.25,
    0
  ]);
});
