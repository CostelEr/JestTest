import { F } from "./29";
test("should return correct results", () => {
  expect(F(5, [3.09, 7.36, 1.15, 9.38, -2.05])).toEqual(3.09);
  expect(
    F(10, [2.13, 4.41, 1.1, -4.58, 7.58, 8.66, 5.35, 1.55, -6.75, 5.34])
  ).toEqual(7.58);
  expect(
    F(10, [-6.41, 8.17, 7.12, 9.62, -4.64, 6.37, 0.54, 6.82, 1.93, -7])
  ).toEqual(7.12);
});
