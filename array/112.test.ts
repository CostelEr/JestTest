import { F } from "./112";
test("should return correct results", () => {
  expect(F(4, [6.34, -0.24, 2.85, 7.51])).toEqual([
    [-0.24, 2.85, 6.34, 7.51],
    [-0.24, 2.85, 6.34, 7.51],
    [-0.24, 2.85, 6.34, 7.51]
  ]);
  expect(F(6, [-2.61, 6.51, -9.72, 2.18, 4.37, -1.32])).toEqual([
    [-2.61, -9.72, 2.18, 4.37, -1.32, 6.51],
    [-9.72, -2.61, 2.18, -1.32, 4.37, 6.51],
    [-9.72, -2.61, -1.32, 2.18, 4.37, 6.51],
    [-9.72, -2.61, -1.32, 2.18, 4.37, 6.51],
    [-9.72, -2.61, -1.32, 2.18, 4.37, 6.51]
  ]);
  expect(F(5, [-5.84, 1.47, 9.02, 2.01, -9.69])).toEqual([
    [-5.84, 1.47, 2.01, -9.69, 9.02],
    [-5.84, 1.47, -9.69, 2.01, 9.02],
    [-5.84, -9.69, 1.47, 2.01, 9.02],
    [-9.69, -5.84, 1.47, 2.01, 9.02]
  ]);
});
