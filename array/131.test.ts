import { F } from "./131";
test("should return correct results", () => {
  expect(
    F(
      8,
      [
        { x: -8.25, y: -8.45 },
        { x: 1.59, y: -6.98 },
        { x: -7.94, y: 1.41 },
        { x: 5.81, y: -3.31 },
        { x: 2.33, y: 9.1 },
        { x: 9.69, y: 2.39 },
        { x: -0.3, y: 5.72 },
        { x: -8.84, y: 7.37 }
      ],
      { x: 2.76, y: -0.43 }
    )
  ).toEqual({ x: 5.81, y: -3.31 });

  expect(
    F(
      5,
      [
        { x: -9.9, y: -5.64 },
        { x: 1.46, y: 4.55 },
        { x: -3.96, y: 9.49 },
        { x: 1.83, y: -2.87 },
        { x: 9.66, y: -3.6 }
      ],
      { x: -1.33, y: 6.64 }
    )
  ).toEqual({ x: 1.46, y: 4.55 });

  expect(
    F(
      4,
      [
        { x: -0.02, y: -6.25 },
        { x: 8.77, y: -4.27 },
        { x: -1.61, y: 2.17 },
        { x: -8.58, y: 5.08 }
      ],
      { x: 3.51, y: 1.5 }
    )
  ).toEqual({ x: -1.61, y: 2.17 });
});
