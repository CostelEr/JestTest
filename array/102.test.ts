import { F } from "./102";
test("should return correct results", () => {
  expect(
    F(9, [-5.57, -8.16, 1.01, 4.09, 2.11, -5.9, -7.43, 7.96, -2.94], 3)
  ).toEqual([-5.57, -8.16, 1.01, 0, 4.09, 2.11, -5.9, -7.43, 7.96, -2.94]);
  expect(F(5, [-4.94, 5.75, -8.9, 3.85, -1.23], 5)).toEqual([
    -4.94,
    5.75,
    -8.9,
    3.85,
    -1.23,
    0
  ]);
  expect(F(8, [-6.04, 3.01, 5.99, 1.06, 0.91, 6.42, 8.11, 6.57], 6)).toEqual([
    -6.04,
    3.01,
    5.99,
    1.06,
    0.91,
    6.42,
    0,
    8.11,
    6.57
  ]);
});
